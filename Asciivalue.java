import java.util.Scanner;
 class Asciivalue
 {
 public static void main(String[] args)
	{
		int	i;
		System.out.println(" ASCII value for small letters ");
        for(i='a';i<='z';i++)
		System.out.println( i );
		System.out.println(" ASCII value for capital letters ");
		for(i='A';i<='Z';i++)
		System.out.println( i );
	}
}
		